#ifndef MATHEMATICAPP_SYMBOL_H
#define MATHEMATICAPP_SYMBOL_H

#include <string>

namespace mathematica{

struct m;

struct symbol{
    symbol(const std::string& name);
    symbol(const symbol& other);
    
    static symbol True();
    static symbol False();
    static symbol Pi();
    static symbol E();
    static symbol I();
    static symbol Infinity();
    static symbol var(const std::string& name);
    
    mathematica::m operator=(const mathematica::m& rhs);
		
    std::string name() const;
    private:
        std::string _name;
};
}

#endif // MATHEMATICAPP_SYMBOL_H
